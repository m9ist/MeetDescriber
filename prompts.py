"""
Шаблоны промптов для анализа и follow-up.

Редактируй этот файл чтобы настроить поведение LLM.

Переменные в USER_TEMPLATE подставляются автоматически:
  {title}        — название совещания
  {date}         — дата (YYYY-MM-DD)
  {agenda_block} — агенда (пустая строка если не задана)
  {transcription} — текст расшифровки (для analysis)
  {analysis}      — текст анализа (для followup)
"""

